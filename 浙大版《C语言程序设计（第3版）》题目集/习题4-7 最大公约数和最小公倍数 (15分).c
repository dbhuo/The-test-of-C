#include <stdio.h>
/*
辗转相除法：
　　(78,14)=(64,14)=(50,14)=(36,14)=(22,14)=(8,14)=(8,6)=(2,6)=(2,4)=(2,2)=(0,2)=2
　　基本上思路就是大数减去小数,一直减到能算出来为止,
	由(78,14)到(8,14)完全可以一步到位,因为(78,14)=(14×5+8,14)=(8,14),由此就诞生出我们的辗转相除法。 
*/
int main()
{
	int m,n;
	if(scanf("%d %d",&m,&n))
	{
		int x = m;
		int y = n;
		int r = 0;
		while(y>0)
		{
			r = x%y;
			x = y;
			y = r;
		}
		printf("%d %d",x,m*n/x);
	}
	return 0;
}
